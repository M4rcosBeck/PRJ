    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        JOptionPane.showConfirmDialog(null, "Deseja excluir a venda?", "Excluir Venda", dialogButton);
        if (dialogButton == JOptionPane.YES_OPTION) {
            int linha = table_vendas.getSelectedRow();
            int codigoVenda = (int) table_vendas.getValueAt(linha, 0);
            listaModeloProduto = new ArrayList<>();
            listaProdutosVendasProdutos = controladorProdutosVendasProdutos.getListaProdutosVendasProdutosControlador(codigoVenda);
            for (int i = 0; i < listaProdutosVendasProdutos.size(); i++) {
                modeloProduto = new Modelo_Produto();
                modeloProduto.setId(listaProdutosVendasProdutos.get(i).getModeloProdutos().getId());
                modeloProduto.setEstoque((int) (listaProdutosVendasProdutos.get(i).getModeloProdutos().getEstoque()
                        + listaProdutosVendasProdutos.get(i).getModeloVendasProdutos().getVendaProQuant()));
                listaModeloProduto.add(modeloProduto);
            }
            if (controladorProduto.alterarEstoqueProdutoControlador(listaModeloProduto)) {
                controladorVendasProdutos.excluirVendasProdutosController(codigoVenda);
                if (controladorVendas.excluirVendasController(codigoVenda)) {
                    JOptionPane.showMessageDialog(this, "Venda excluída com sucesso!", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
                    this.carregarVendas();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir a venda!", "ERRO", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir a venda!", "ERRO", JOptionPane.WARNING_MESSAGE);
            }
        if (dialogButton == JOptionPane.NO_OPTION) {
            remove(dialogButton);
        }
        }
    }//GEN-LAST:event_btExcluirActionPerformed


    private void carregarVendas() {
        DefaultTableModel modelo = (DefaultTableModel) table_vendas.getModel();
        listaModelVendasClientes = controladorVendasCliente.getListaVendasCliente();

        int cont = listaModelVendasClientes.size();
        modelo.setNumRows(0);
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelVendasClientes.get(i).getModeloVendas().getIdVendas(),
                listaModelVendasClientes.get(i).getModeloClientes().getNomeCliente(),
                listaModelVendasClientes.get(i).getModeloVendas().getVendaDataVenda()
            });
        }
    }


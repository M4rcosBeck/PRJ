    /**
     * FAZ O C√ÅLCULO FINAL DA VENDA
     */
    private void calcularPagamento() {
        float subTotal, desconto, recebido, valorPagar, troco;

        try {
            //formatador.converteVirgulaparaPonto(String.valueOf(subTotal));
            subTotal = Float.parseFloat(txtFinalizarSubtotal.getText());
        } catch (Exception e) {
            subTotal = 0;
        }

        try {
            //formatador.converteVirgulaparaPonto(String.valueOf(desconto));
            desconto = Float.parseFloat((txtFinalizarDesconto.getText()));
        } catch (Exception e) {
            desconto = 0;
            txtFinalizarDesconto.setText("0");
        }
        
        try {
            //formatador.converteVirgulaparaPonto(String.valueOf(recebido));
            recebido = Float.parseFloat(txtFinalizarRecebido.getText());
        } catch (Exception e) {
            recebido = 0;
            txtFinalizarRecebido.setText("0");
        }

        //calcular valor a pagar
        valorPagar = subTotal - desconto;
        //labelValorTotal.setText(arredondar(valorPagar));
        labelValorTotal.setText(String.valueOf(valorPagar));

        //calcular o troco
        troco = recebido - valorPagar;
        //txtFinalizarTroco.setText(arredondar(troco));
        txtFinalizarTroco.setText(String.valueOf(troco));
        //troco = Float.parseFloat(txtFinalizarTroco.getText());
    }
    
    public String arredondar(float pArredondar){
        DecimalFormat df = new DecimalFormat("#.00");
        return df.format(pArredondar);
    }


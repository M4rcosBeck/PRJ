    private void btEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnviarActionPerformed
        try {
            String m = txtTexto.getText(), mm = m;
            String CI = clientID;

            if (!clientID.isEmpty()) {
                m = "#4344554@@@@@67667@@" + CI + ":" + mm;
                dout.writeUTF(m);
                txtTexto.setText("");
                msgBox.append("(PRIVADO para " + CI + "): " + mm + "\n");
                updateMsgBox();
            } else {
                dout.writeUTF(m);
                txtTexto.setText("");
                msgBox.append(iD + ": " + mm + "\n");
                updateMsgBox();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Servidor Offline ou o usuário selecionado foi deslogado!", "Falha ao Enviar", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btEnviarActionPerformed


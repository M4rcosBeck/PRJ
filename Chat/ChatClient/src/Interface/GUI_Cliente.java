    GUI_Cliente(String i, Socket s) {
        iD = i;
        try {
            initComponents();
            dlm = new DefaultListModel();
            list_UserBox.setModel(dlm);
            lblUser.setText("  " + i);
            din = new DataInputStream(s.getInputStream());
            dout = new DataOutputStream(s.getOutputStream());
            new Read().start();

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    class Read extends Thread {

        public void run() {
            while (true) {
                try {
                    String m = din.readUTF();
                    if (m.contains(":;.,/=")) {
                        m = m.substring(6);
                        dlm.clear();
                        StringTokenizer st = new StringTokenizer(m, ",");
                        while (st.hasMoreTokens()) {
                            String u = st.nextToken();
                            if (!iD.equals(u)) {
                                dlm.addElement(u);
                            }
                        }
                    } else if (m.contains("@#$ddd@#$")) {
                        m = m.substring(9);
                        lblDigitando.setText(m);
                        Thread.sleep(300);
                        lblDigitando.setText("...");
                    } else {
                        msgBox.append("" + m + "\n");
                    }
                } catch (Exception e) {
                    break;
                }
            }
        }
    }

